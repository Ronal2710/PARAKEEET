package pe.edu.upc.entity;

public class Persona {
private int CPersona;
private String NPersona;
private String NApellido;
private String Correo;
private int DNI;
private String TDireccion;
public int getCPersona() {
	return CPersona;
}
public void setCPersona(int cPersona) {
	CPersona = cPersona;
}
public String getNPersona() {
	return NPersona;
}
public void setNPersona(String nPersona) {
	NPersona = nPersona;
}
public String getNApellido() {
	return NApellido;
}
public void setNApellido(String nApellido) {
	NApellido = nApellido;
}
public String getCorreo() {
	return Correo;
}
public void setCorreo(String correo) {
	Correo = correo;
}
public int getDNI() {
	return DNI;
}
public void setDNI(int dNI) {
	DNI = dNI;
}
public String getTDireccion() {
	return TDireccion;
}
public void setTDireccion(String tDireccion) {
	TDireccion = tDireccion;
}
}
